	static function parse($url,$html)
	{		
		$thiz=self::getInstance();
		$type = get_class();			
		$x = new Xpath($html);	
		
		$type = get_class();	

		$ap = new Address_Parser();
		$pp = new phone_parser();
		$ep = new Email_Parser();
		$np = new Name_Parser();
		$kvp = new KeyValue_Parser();

		
		$row = array();
		$data = array();
		$i=0;
		foreach ($x->query("//table//td//text()") as $node)
		{			
			if ($i++ < 3) continue;
			$row[]= $node->textContent;
//			$row = array_merge($row,explode("<br>",$node->c14n()));
			if ($i > 20) break;
		}

	
		$data['OWNER_NAME'] = self::cleanup($row[0]);
		$data['COMPANY_NAME'] = self::cleanup($row[2]);
		
		$data = array_merge($data,$ap->parse(self::cleanup("{$row[3]}, {$row[4]}")));
	
		$offset = 0;
		if (trim($row[5]) == 'United States')
			$offset += 1;
		$data = array_merge($data,$kvp->parse($row[5+$offset]));
		$data = array_merge($data,$kvp->parse($row[6+$offset]));

		$data['EMAIL']  = $row[8+$offset];
		$data['WEBSITE']  = $row[11+$offset];

		$data['SOURCE_URL'] = $url;

		log::info($data);								
		db::store($type,$data,array('SOURCE_URL'),false);
	

	}

